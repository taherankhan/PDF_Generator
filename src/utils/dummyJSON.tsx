// Helper function to generate booking data for a user
const generateUserBookings = (
    userId: string,
    count: number,
    startBookingId: number
): Array<{
    _id: string;
    userId: string;
    entityName: string;
    type: string;
    status: "Ongoing" | "Waiting for check in" | "Completed" | "Cancelled";
    createdAt: string;
    timeSlot: {start: string; end: string};
    hoursCharged: number;
    kwCharged: number;
    isCharging: boolean;
    isLinkedParkingSpace: boolean;
    isQRCode?: boolean;
    bookedBy?: {name: string; email: string};
    amount?: number;
}> => {
    const bookings: any[] = [];
    const statuses: Array<"Ongoing" | "Waiting for check in" | "Completed" | "Cancelled"> = [
        "Ongoing",
        "Waiting for check in",
        "Completed",
        "Cancelled",
    ];
    const types = ["Charging only", "Parking only", "Combined"];
    const entityNames = [
        "GF Charge Hub - Central",
        "GF Parking Bay A-102",
        "GF Supercharger A3",
        "GF Open Lot C-33",
        "GF ChargePoint West",
        "GF Reserved Bay R-14",
        "GF DC Fast Station 7",
        "GF MultiStorey L3-17",
        "GF Urban Charger S2",
        "GF Visitor Slot V-05",
        "GF Premium Charger P1",
        "GF Express Bay E-22",
    ];

    // Calculate distribution: 20% Ongoing, 10% Waiting, 50% Completed, 20% Cancelled
    const ongoingCount = Math.ceil(count * 0.2);
    const waitingCount = Math.ceil(count * 0.1);
    const completedCount = Math.ceil(count * 0.5);
    const cancelledCount = count - ongoingCount - waitingCount - completedCount;

    let bookingId = startBookingId;
    const now = new Date();

    // Generate Ongoing bookings
    for (let i = 0; i < ongoingCount && bookingId < startBookingId + count; i++) {
        const daysAgo = Math.floor(Math.random() * 7);
        const startDate = new Date(now.getTime() - daysAgo * 24 * 60 * 60 * 1000);
        const hours = Math.floor(Math.random() * 4) + 1;
        const endDate = new Date(startDate.getTime() + hours * 60 * 60 * 1000);
        const type = types[Math.floor(Math.random() * types.length)];
        const isCharging = type !== "Parking only";
        const isQRCode = Math.random() > 0.3; // 70% QR code bookings
        const hoursCharged = isCharging ? Number((hours * (0.7 + Math.random() * 0.6)).toFixed(1)) : 0;
        const kwCharged = isCharging ? Math.floor(hours * (15 + Math.random() * 25)) : 0;

        // Calculate amount: charging rate €0.35/kWh, parking rate €2/hour, combined gets both
        let amount = 0;
        if (type === "Charging only") {
            amount = Number((kwCharged * 0.35 + hours * 0.5).toFixed(2)); // €0.35/kWh + base fee
        } else if (type === "Parking only") {
            amount = Number((hours * 2.0 + Math.random() * 1).toFixed(2)); // €2/hour + small variation
        } else if (type === "Combined") {
            amount = Number((kwCharged * 0.35 + hours * 2.0 + Math.random() * 1.5).toFixed(2)); // Both rates
        }

        bookings.push({
            _id: `UB-${bookingId++}`,
            userId,
            entityName: entityNames[Math.floor(Math.random() * entityNames.length)],
            type,
            status: "Ongoing",
            createdAt: startDate.toISOString(),
            timeSlot: {start: startDate.toISOString(), end: endDate.toISOString()},
            hoursCharged,
            kwCharged,
            isCharging,
            isLinkedParkingSpace: type === "Combined" || (type === "Parking only" && Math.random() > 0.5),
            isQRCode: isQRCode || undefined,
            amount,
        });
    }

    // Generate Waiting for check in bookings
    for (let i = 0; i < waitingCount && bookingId < startBookingId + count; i++) {
        const daysAhead = Math.floor(Math.random() * 5) + 1;
        const startDate = new Date(now.getTime() + daysAhead * 24 * 60 * 60 * 1000);
        const hours = Math.floor(Math.random() * 4) + 1;
        const endDate = new Date(startDate.getTime() + hours * 60 * 60 * 1000);
        const type = types[Math.floor(Math.random() * types.length)];
        const isCharging = type !== "Parking only";

        // Estimated amount for future bookings
        let amount = 0;
        const estimatedKw = isCharging ? hours * 20 : 0; // Estimate 20kW per hour
        if (type === "Charging only") {
            amount = Number((estimatedKw * 0.35 + hours * 0.5).toFixed(2));
        } else if (type === "Parking only") {
            amount = Number((hours * 2.0 + Math.random() * 1).toFixed(2));
        } else if (type === "Combined") {
            amount = Number((estimatedKw * 0.35 + hours * 2.0 + Math.random() * 1.5).toFixed(2));
        }

        bookings.push({
            _id: `UB-${bookingId++}`,
            userId,
            entityName: entityNames[Math.floor(Math.random() * entityNames.length)],
            type,
            status: "Waiting for check in",
            createdAt: new Date(now.getTime() - Math.floor(Math.random() * 7) * 24 * 60 * 60 * 1000).toISOString(),
            timeSlot: {start: startDate.toISOString(), end: endDate.toISOString()},
            hoursCharged: 0,
            kwCharged: 0,
            isCharging,
            isLinkedParkingSpace: type === "Combined",
            amount,
        });
    }

    // Generate Completed bookings
    for (let i = 0; i < completedCount && bookingId < startBookingId + count; i++) {
        const daysAgo = Math.floor(Math.random() * 60) + 1;
        const startDate = new Date(now.getTime() - daysAgo * 24 * 60 * 60 * 1000);
        const hours = Math.floor(Math.random() * 5) + 1;
        const endDate = new Date(startDate.getTime() + hours * 60 * 60 * 1000);
        const type = types[Math.floor(Math.random() * types.length)];
        const isCharging = type !== "Parking only";
        const isQRCode = Math.random() > 0.25; // 75% QR code bookings
        const hoursCharged = isCharging ? Number((hours * (0.8 + Math.random() * 0.4)).toFixed(1)) : 0;
        const kwCharged = isCharging ? Math.floor(hours * (18 + Math.random() * 22)) : 0;

        // Calculate amount for completed bookings
        let amount = 0;
        if (type === "Charging only") {
            amount = Number((kwCharged * 0.35 + hours * 0.5 + Math.random() * 0.5).toFixed(2));
        } else if (type === "Parking only") {
            amount = Number((hours * 2.0 + Math.random() * 1.5).toFixed(2));
        } else if (type === "Combined") {
            amount = Number((kwCharged * 0.35 + hours * 2.0 + Math.random() * 2).toFixed(2));
        }

        bookings.push({
            _id: `UB-${bookingId++}`,
            userId,
            entityName: entityNames[Math.floor(Math.random() * entityNames.length)],
            type,
            status: "Completed",
            createdAt: startDate.toISOString(),
            timeSlot: {start: startDate.toISOString(), end: endDate.toISOString()},
            hoursCharged,
            kwCharged,
            isCharging,
            isLinkedParkingSpace: type === "Combined" || (type === "Parking only" && Math.random() > 0.4),
            isQRCode: isQRCode || undefined,
            amount,
        });
    }

    // Generate Cancelled bookings
    for (let i = 0; i < cancelledCount && bookingId < startBookingId + count; i++) {
        const daysAgo = Math.floor(Math.random() * 30) + 1;
        const startDate = new Date(now.getTime() - daysAgo * 24 * 60 * 60 * 1000);
        const hours = Math.floor(Math.random() * 4) + 1;
        const endDate = new Date(startDate.getTime() + hours * 60 * 60 * 1000);
        const type = types[Math.floor(Math.random() * types.length)];

        // Cancelled bookings typically have refunded amount (0) or cancellation fee
        const hasCancellationFee = Math.random() > 0.7; // 30% chance of cancellation fee
        const estimatedKw = type !== "Parking only" ? hours * 20 : 0;
        let amount = 0;
        if (hasCancellationFee) {
            // Small cancellation fee (10-20% of estimated booking)
            if (type === "Charging only") {
                amount = Number(((estimatedKw * 0.35 + hours * 0.5) * (0.1 + Math.random() * 0.1)).toFixed(2));
            } else if (type === "Parking only") {
                amount = Number((hours * 2.0 * (0.1 + Math.random() * 0.1)).toFixed(2));
            } else if (type === "Combined") {
                amount = Number(((estimatedKw * 0.35 + hours * 2.0) * (0.1 + Math.random() * 0.1)).toFixed(2));
            }
        }

        bookings.push({
            _id: `UB-${bookingId++}`,
            userId,
            entityName: entityNames[Math.floor(Math.random() * entityNames.length)],
            type,
            status: "Cancelled",
            createdAt: startDate.toISOString(),
            timeSlot: {start: startDate.toISOString(), end: endDate.toISOString()},
            hoursCharged: 0,
            kwCharged: 0,
            isCharging: type !== "Parking only",
            isLinkedParkingSpace: false,
            amount,
        });
    }

    return bookings.sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime());
};

